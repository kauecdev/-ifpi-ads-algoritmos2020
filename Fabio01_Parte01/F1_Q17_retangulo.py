# Entrada

base_retangulo = float(input("Informe o valor da base do retângulo: "))
altura_retangulo = float(input("Informe o valor da altura do retângulo: "))

# Processamento

area_retangulo = base_retangulo * altura_retangulo

# Saída

print("A área do retângulo com base",base_retangulo,"e altura",altura_retangulo,"é igual a: ",area_retangulo)