# Entrada

first_numb = int(input("Digite o primeiro valor: "))
second_numb = int(input("Digite o segundo valor: "))
third_numb = int(input("Digite o terceiro valor: "))

# Processamento 

soma = first_numb + second_numb
diferenca = second_numb - third_numb

# Saída 

print("A soma entre",first_numb,"e",second_numb,"é :",soma,". Já a diferença entre",second_numb,"e",third_numb,"é :", diferenca,".")
