# Entrada

first_numb = int(input("Digite o primeiro valor: "))
second_numb = int(input("Digite o segundo valor: "))

# Processamento

quociente = first_numb / second_numb
resto_div = first_numb % second_numb

# Saída

print("O quociente da divisão entre",first_numb,"e",second_numb,"é :",quociente,". Já o resto desta divisão é:",resto_div)